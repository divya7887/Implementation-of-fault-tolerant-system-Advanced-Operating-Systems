//package replicamain;
public class Votes {

	int nodeId;
	int votes;
	
	
	public Votes(int nodeId, int votes) {
		
		this.nodeId = nodeId;
		this.votes = votes;
	}
	public int getNodeId() {
		return nodeId;
	}
	public void setNodeId(int nodeId) {
		this.nodeId = nodeId;
	}
	public int getVotes() {
		return votes;
	}
	public void setVotes(int votes) {
		this.votes = votes;
	}
	
	
	
	
}
